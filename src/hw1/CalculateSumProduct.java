package hw1;

public class CalculateSumProduct {
	
	//請設計一隻Java程式,計算12,6這兩個數值的和與積
	
	public static void main(String[] args) {
		
		int num1 = 12;
		int num2 = 6;
		
		int sum = num1 + num2;
		int product = num1 * num2;
		
		System.out.println("12與6的和為:" + sum);
		System.out.println("12與6的積為:" + product);

	}

}
